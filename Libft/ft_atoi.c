int	ft_atoi(const char *str)
{
	size_t	i;
	int		v;
	char	c;

	i = 0;
	v = 0;
	while (str[i])
	{
		c = str[i];
		if (c >= '0' || c <= '9')
			v = v + (10 * i) + c - '0';
		i++;
	}
	if (str[0] == '-')
		v *= -1;
	return (v);
}
